# Program to check multiple data types using var1, var2...

var1 = 10              # int
var2 = 3.14            # float
var3 = "Hello"         # string
var4 = True            # boolean
var5 = [1, 2, 3]       # list

print("Type of var1:", type(var1))
print("Type of var2:", type(var2))
print("Type of var3:", type(var3))
print("Type of var4:", type(var4)) 
print("Type of var5:", type(var5))
