# Integer to float
num_int = 10
num_float = float(num_int)
print("Integer to Float:", num_float)

# Float to integer
num_float = 12.7
num_int = int(num_float)
print("Float to Integer:", num_int)

# String to integer
num_str = "25"
num_int = int(num_str)
print("String to Integer:", num_int)

# integer to string
num_int = 50
num_str = str(num_int)
print("Integer to String:", num_str)

# String to float
num_str = "45.67"
num_float = float(num_str)
print("String to Float:", num_float)
